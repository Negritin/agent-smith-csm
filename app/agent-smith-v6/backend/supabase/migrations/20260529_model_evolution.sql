-- (d) Orphan-agent remap (Design Lock #4 / SPRINTS MAJOR-2).
--
--     Existing agents may have stored a bare "Outros" id as llm_model (grok-4,
--     grok-3, deepseek-chat, mistral-large-latest). These fall into the wrong
--     billing/factory path. We remap each to its OpenRouter slug and set
--     llm_provider='openrouter'. Each UPDATE is GUARDED by EXISTS so it only
--     fires when the target OpenRouter slug actually exists in llm_pricing
--     (it does, after section b).
--
--     If ZERO agents match (the normal case on a fresh install, and common on
--     existing installs that never used "Outros"), each statement is a no-op.
--     This is expected and not an error.
--
--     HUMAN REVIEW REQUIRED: grok-4 and grok-3 have no live OpenRouter slug in
--     our table (x-ai/grok-4 / x-ai/grok-3 are not present), so both are mapped
--     to the closest available slug 'x-ai/grok-2'. Confirm this mapping (pricing
--     and behavior differ from grok-4) before relying on it in production.
-- -----------------------------------------------------------------------------

-- grok-4 -> x-ai/grok-2  (HUMAN REVIEW: closest available slug, not exact grok-4)
UPDATE public.agents
SET llm_model = 'x-ai/grok-2', llm_provider = 'openrouter'
WHERE llm_model = 'grok-4'
  AND EXISTS (SELECT 1 FROM public.llm_pricing WHERE model_name = 'x-ai/grok-2');

-- grok-3 -> x-ai/grok-2  (HUMAN REVIEW: closest available slug, not exact grok-3)

-- mistral-large-latest -> mistralai/mistral-large
UPDATE public.agents
SET llm_model = 'mistralai/mistral-large', llm_provider = 'openrouter'
WHERE llm_model = 'mistral-large-latest'
  AND EXISTS (SELECT 1 FROM public.llm_pricing WHERE model_name = 'mistralai/mistral-large');

-- Catch-all safety net: any remaining agent whose provider is outside the four
-- known providers AND whose model happens to match an OpenRouter slug already in
-- llm_pricing gets its provider corrected to 'openrouter'. Guarded so it only
-- touches rows with a real pricing match; otherwise a no-op. Bare ids handled
-- above are already remapped, so they will not be caught here.
UPDATE public.agents a
SET llm_provider = 'openrouter'
WHERE a.llm_provider IS NOT NULL
  AND a.llm_provider NOT IN ('openai', 'anthropic', 'google', 'openrouter')
  AND EXISTS (
        SELECT 1 FROM public.llm_pricing p
        WHERE p.model_name = a.llm_model
          AND p.provider = 'openrouter'
  );

